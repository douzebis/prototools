// ── `raw_range` fidelity ────────────────────────────────────────────────────

#[test]
fn raw_range_fidelity_reslices_and_redecodes() {
    let schema = any_schema();
    let wire = any_wire_bytes();

    let (_text, spans) = with_any_loader(&schema, || {
        decode_and_render_indexed(
            &wire,
            schema.root_descriptor().as_ref(),
            DecodeRenderOpts {
                annotations: true,
                emit_header: true,
                ..Default::default()
            },
        )
    });

    // A nested (not top-level) node: the Any's `value {}` wrapper, resolved
    // to acme.Payload.
    let value = spans
        .iter()
        .find(|s| s.field_number == 0 && s.level == 1)
        .expect("value{} wrapper span");
    assert_eq!(value.type_fqdn.as_deref(), Some("acme.Payload"));

    // Slice `value`'s raw_range directly from the original top-level buf —
    // absolute, no re-slicing through ancestors needed — and independently
    // re-decode it under its own resolved type.
    let sub_slice = &wire[value.raw_range.clone()];

    let payload_schema =
        schema_from_pool(schema.pool().clone(), "acme.Payload").expect("payload schema");
    let redecoded = prototext_core::render_as_text(
        sub_slice,
        payload_schema.root_descriptor().as_ref(),
        prototext_core::RenderOpts {
            assume_binary: true,
            include_annotations: false,
            indent: 1,
            expand_any: true,
            hide_unknown_fields: false,
            expand_message_set: true,
        },
    )
    .expect("re-decode sliced raw_range");
    let redecoded = String::from_utf8(redecoded).unwrap();
    assert!(
        redecoded.contains("hello"),
        "expected re-decoded raw_range slice to contain label 'hello': {redecoded}"
    );
}
